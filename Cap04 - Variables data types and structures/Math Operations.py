'''
Um inteiro dividido por outro inteiro utlizando uma unica barra resulta em
um número do tipo float
'''
print(4/2)

'''
Um inteiro dividido por outro inteiro utilizando duas barras resulta em um
número inteiro
'''
print(4//2)

'''
Um inteiro dividido por um float utlizando uma barra resulta em um 
float

'''
print(4/3.0)

'''
Um inteiro dividido por float utlizando duas barras resulta em um float 
truncado

'''
print(4//3.0)


#Conversões entre tipos

'''
Convertendo um inteiro para float
'''
print(float (9))

'''
Convertendo float para inteiro
'''
print(int(6.5))

#Conversões entre bases

'''
Convertendo decimal para hexadecimal
'''
print(f"O número 324 em hexadecimal é: {hex(324)}")


'''
Convertendo decimal para binário

'''

print(f"O número 324 em binário é: {bin(324)}")


#Functions abs, roud e pow

'''
Determinando o valor absoluto de um número
'''
print(f"O absoluto do número -8 é: {abs(-8)}")


'''
Arredondando um número e especificando a quantidade de 
casas decimais
'''
print(f"O número 3.14280482930934 arredondado em duas casas decimais é: {round(3.14280482930934,2)}")


'''
Potenciação
'''

print(f"A potência do número 2 elevado ao quadrado é: {pow(2,2)}")





